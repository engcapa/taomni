def openpgp_primary_fingerprint(public_key: Path) -> str:
    with tempfile.TemporaryDirectory(
        prefix="taomni-linux-gate-gpg-", dir="/tmp"
    ) as directory:
        os.chmod(directory, 0o700)
        result = run_tool(
            "gpg",
            [
                "--batch",
                "--no-options",
                "--homedir",
                directory,
                "--with-colons",
                "--import-options",
                "show-only",
                "--dry-run",
                "--import",
                os.fspath(public_key),
            ],
        )
    fingerprints: list[str] = []
    awaiting_primary = False
    for line in result.stdout.splitlines():
        fields = line.split(":")
        record = fields[0] if fields else ""
        if record == "pub":
            awaiting_primary = True
        elif record == "sub":
            awaiting_primary = False
        elif record == "fpr" and awaiting_primary and len(fields) > 9:
            fingerprints.append(normalize_fingerprint(fields[9], "public key fingerprint"))
            awaiting_primary = False
    if len(fingerprints) != 1:
        fail("LINUX_SIGNING_KEY_INVALID", "public key file must contain exactly one primary OpenPGP key")
    return fingerprints[0]


def verify_deb_signature(package: Path, signature: Path, public_key: Path, expected_fingerprint: str) -> str:
    with tempfile.TemporaryDirectory(
        prefix="taomni-linux-gate-gpg-", dir="/tmp"
    ) as directory:
        os.chmod(directory, 0o700)
        run_tool(
            "gpg",
            ["--batch", "--no-options", "--homedir", directory, "--import", os.fspath(public_key)],
        )
        # GnuPG 2.4 imports into pubring.kbx, while gpgv's legacy default is
        # trustedkeys.kbx. Export the one pinned key into an explicit packet
        # keyring so verification never depends on either default filename.
        keyring = Path(directory) / "release-signing-key.gpg"
        run_tool(
            "gpg",
            [
                "--batch",
                "--no-options",
                "--homedir",
                directory,
                "--output",
                os.fspath(keyring),
                "--export-options",
                "export-minimal",
                "--export",
                expected_fingerprint,
            ],
        )
        result = run_tool(
            "gpgv",
            [
                "--homedir",
                directory,
                "--keyring",
                os.fspath(keyring),
                "--status-fd",
                "1",
                os.fspath(signature),
                os.fspath(package),
            ],
        )
    valid_lines = [line for line in result.stdout.splitlines() if line.startswith("[GNUPG:] VALIDSIG ")]
    if len(valid_lines) != 1:
        fail("LINUX_PACKAGE_SIGNATURE_INVALID", "detached signature did not emit one VALIDSIG proof")
    fingerprints = {
        token.upper()
        for token in valid_lines[0].split()[2:]
        if FINGERPRINT_RE.fullmatch(token)
    }
    if expected_fingerprint.upper() not in fingerprints:
        fail("LINUX_PACKAGE_SIGNER_MISMATCH", "detached signature signer differs from fixed policy")
    return expected_fingerprint.upper()


def verify_rpm_signature(package: Path, public_key: Path, expected_fingerprint: str) -> str:
    with tempfile.TemporaryDirectory(
        prefix="taomni-linux-gate-rpm-", dir="/tmp"
    ) as directory:
        database = Path(directory) / "rpmdb"
        database.mkdir(mode=0o700)
        run_tool("rpm", ["--noplugins", "--dbpath", os.fspath(database), "--initdb"])
        run_tool(
            "rpmkeys",
            ["--noplugins", "--dbpath", os.fspath(database), "--import", os.fspath(public_key)],
        )
        result = run_tool(
            "rpmkeys",
            [
                "--noplugins",
                "--dbpath",
                os.fspath(database),
                "--checksig",
                "--verbose",
                os.fspath(package),
            ],
        )
    output = f"{result.stdout}\n{result.stderr}".lower()
    if re.search(r"\b(?:not\s+ok|notsigned|no\s+key|nokey)\b", output):
        fail("LINUX_PACKAGE_SIGNATURE_INVALID", "RPM signature output reports an untrusted package")
    if not (
        re.search(r"signature[^\n]*:\s*ok\b", output)
        or re.search(r"\bsignatures\s+ok\b", output)
    ):
        fail("LINUX_PACKAGE_SIGNATURE_INVALID", "RPM has no verified embedded OpenPGP signature")
    return expected_fingerprint.upper()


def normalize_package_path(value: str) -> str:
    path = PurePosixPath(value)
    parts = [part for part in path.parts if part not in {"/", ".", ""}]
    if not parts or ".." in parts or "\x00" in value:
        fail("LINUX_PACKAGE_CONTENT_INVALID", f"unsafe package member path {value!r}")
    return "/" + "/".join(parts)


def parse_deb_dependency_atom(value: str) -> str:
    atom = value.strip()
    while re.search(r"\s+<[^<>]+>\s*$", atom):
        atom = re.sub(r"\s+<[^<>]+>\s*$", "", atom)
    atom = re.sub(r"\s+\[[^\[\]]+\]\s*$", "", atom)
    atom = re.sub(r"\s+\([^()]+\)\s*$", "", atom)
    match = re.fullmatch(r"([a-z0-9][a-z0-9+.-]*)(?::[A-Za-z0-9][A-Za-z0-9-]*)?", atom)
    if match is None:
        fail("LINUX_PACKAGE_DEPENDENCY_INVALID", f"malformed DEB dependency atom {value!r}")
    return match.group(1)


def parse_deb_dependencies(value: str) -> dict[str, list[str]]:
    if "\x00" in value or len(value.encode("utf-8")) > MAX_POLICY_BYTES:
        fail("LINUX_PACKAGE_DEPENDENCY_INVALID", "DEB dependency metadata is unsafe or too large")
    requirements = [item.strip() for item in value.replace("\n", " ").split(",") if item.strip()]
    direct_packages: list[str] = []
    for requirement in requirements:
        alternatives = [item.strip() for item in requirement.split("|")]
        if any(not item for item in alternatives):
            fail("LINUX_PACKAGE_DEPENDENCY_INVALID", f"malformed DEB dependency {requirement!r}")
        names = [parse_deb_dependency_atom(item) for item in alternatives]
        if len(names) == 1:
            direct_packages.append(names[0])
    return {
        "requirements": requirements,
        "directPackages": sorted(set(direct_packages)),
    }


def parse_rpm_dependencies(value: str) -> dict[str, list[str]]:
    if "\x00" in value or len(value.encode("utf-8")) > MAX_POLICY_BYTES:
        fail("LINUX_PACKAGE_DEPENDENCY_INVALID", "RPM dependency metadata is unsafe or too large")
    requirements = [line.strip() for line in value.splitlines() if line.strip()]
    direct_packages: list[str] = []
    for requirement in requirements:
        if any(ord(character) < 0x20 and character != "\t" for character in requirement):
            fail("LINUX_PACKAGE_DEPENDENCY_INVALID", "RPM dependency metadata contains control bytes")
        name = requirement.split(None, 1)[0]
        if not name or len(name) > 512:
            fail("LINUX_PACKAGE_DEPENDENCY_INVALID", f"malformed RPM dependency {requirement!r}")
        direct_packages.append(name)
    return {
        "requirements": requirements,
        "directPackages": sorted(set(direct_packages)),
    }


def package_path_is_archive_root(value: str) -> bool:
    return not [part for part in PurePosixPath(value).parts if part not in {"/", ".", ""}]


def tar_entry_type(member: tarfile.TarInfo) -> str:
    if member.isfile():
        return "file"
    if member.isdir():
        return "directory"
    if member.issym():
        return "symlink"
    if member.islnk():
        return "hardlink"
    if member.ischr():
        return "char_device"
    if member.isblk():
        return "block_device"
    if member.isfifo():
        return "fifo"
    return "other"


def normalize_package_script(content: bytes, label: str) -> bytes:
    if not content or len(content) > MAX_PACKAGE_SCRIPT_BYTES:
        fail("LINUX_PACKAGE_SCRIPT_INVALID", f"{label} must be a non-empty script of at most 1 MiB")
    try:
        text = content.decode("utf-8")
    except UnicodeDecodeError as error:
        fail("LINUX_PACKAGE_SCRIPT_INVALID", f"{label} is not UTF-8: {error}")
    if text.startswith("\ufeff") or "\x00" in text:
        fail("LINUX_PACKAGE_SCRIPT_INVALID", f"{label} contains a BOM or NUL byte")
    normalized = text.replace("\r\n", "\n").replace("\r", "\n").rstrip("\n") + "\n"
    return normalized.encode("utf-8")


def package_script_evidence(content: bytes, label: str, interpreter: str) -> dict[str, Any]:
    normalized = normalize_package_script(content, label)
    if normalized.split(b"\n", 1)[0] != f"#!{interpreter}".encode("utf-8"):
        fail("LINUX_PACKAGE_SCRIPT_INVALID", f"{label} must start with #!{interpreter}")
    return {
        "rawSha256": hashlib.sha256(content).hexdigest(),
        "normalizedSha256": hashlib.sha256(normalized).hexdigest(),
        "size": len(content),
        "interpreter": interpreter,
    }


def inspect_deb_control_scripts(package: Path) -> dict[str, dict[str, Any]]:
    tool = resolve_tool("dpkg-deb")
    required_phases = set(PACKAGE_SCRIPT_MAPPINGS["deb"])
    scripts: dict[str, dict[str, Any]] = {}
    seen: set[str] = set()
    with tempfile.TemporaryFile() as diagnostics:
        process = subprocess.Popen(
            [tool, "--ctrl-tarfile", os.fspath(package)],
            stdout=subprocess.PIPE,
            stderr=diagnostics,
            env=hermetic_tool_environment(),
        )
        assert process.stdout is not None
        try:
            with tarfile.open(fileobj=process.stdout, mode="r|*") as archive:
                for member in archive:
                    if package_path_is_archive_root(member.name):
                        continue
                    path = normalize_package_path(member.name)
                    if path in seen:
                        fail("LINUX_PACKAGE_SCRIPT_INVALID", f"duplicate DEB control member {path}")
                    seen.add(path)
                    name = path.removeprefix("/")
                    if "/" in name:
                        fail("LINUX_PACKAGE_SCRIPT_INVALID", f"nested DEB control member is forbidden: {path}")
                    if name not in required_phases:
                        if name not in DEB_ALLOWED_CONTROL_MEMBERS:
                            fail("LINUX_PACKAGE_SCRIPT_INVALID", f"unpinned DEB control member {path}")
                        if (
                            not member.isfile()
                            or member.mode & (0o111 | stat.S_ISUID | stat.S_ISGID | stat.S_IWGRP | stat.S_IWOTH)
                            or member.uid != 0
                            or member.gid != 0
                            or member.size <= 0
                            or member.size > MAX_PACKAGE_SCRIPT_BYTES
                        ):
                            fail("LINUX_PACKAGE_SCRIPT_INVALID", f"unsafe DEB control metadata member {path}")
                        continue
                    if (
                        not member.isfile()
                        or not member.mode & 0o111
                        or member.mode
                        & (
                            stat.S_ISUID
                            | stat.S_ISGID
                            | stat.S_IWGRP
                            | stat.S_IWOTH
                        )
                        or member.uid != 0
                        or member.gid != 0
                    ):
                        fail("LINUX_PACKAGE_SCRIPT_INVALID", f"DEB {name} must be an executable regular script")
                    if member.size <= 0 or member.size > MAX_PACKAGE_SCRIPT_BYTES:
                        fail("LINUX_PACKAGE_SCRIPT_INVALID", f"DEB {name} has an invalid size")
                    source = archive.extractfile(member)
                    if source is None:
                        fail("LINUX_PACKAGE_SCRIPT_INVALID", f"cannot read DEB {name}")
                    content = source.read(MAX_PACKAGE_SCRIPT_BYTES + 1)
                    if len(content) != member.size:
                        fail("LINUX_PACKAGE_SCRIPT_INVALID", f"DEB {name} was not read completely")
                    scripts[name] = package_script_evidence(content, f"DEB {name}", "/bin/sh")
        except LinuxGateError:
            process.kill()
            process.wait()
            raise
        except (tarfile.TarError, OSError) as error:
            process.kill()
            process.wait()
            fail("LINUX_PACKAGE_SCRIPT_INVALID", f"cannot inspect DEB control archive: {error}")
        finally:
            process.stdout.close()
        return_code = process.wait()
        if return_code != 0:
            diagnostics.seek(0)
            detail = diagnostics.read(2000).decode("utf-8", "replace")
            fail("LINUX_PACKAGE_SCRIPT_INVALID", f"dpkg-deb control extraction failed: {detail}")
    if set(scripts) != required_phases:
        fail(
            "LINUX_PACKAGE_SCRIPT_INVALID",
            f"DEB maintainer scripts differ: missing={sorted(required_phases - set(scripts))}",
        )
    if "/control" not in seen:
        fail("LINUX_PACKAGE_SCRIPT_INVALID", "DEB control archive omits the control member")
    return scripts


def inspect_deb_package(package: Path, expected_name: str, expected_architecture: str) -> dict[str, Any]:
    package_name = run_tool("dpkg-deb", ["-f", os.fspath(package), "Package"]).stdout.strip()
    version = run_tool("dpkg-deb", ["-f", os.fspath(package), "Version"]).stdout.strip()
    deb_arch = run_tool("dpkg-deb", ["-f", os.fspath(package), "Architecture"]).stdout.strip()
    dependency_metadata = run_tool("dpkg-deb", ["-f", os.fspath(package), "Depends"]).stdout.strip()
    for field in DEB_FORBIDDEN_RELATIONSHIP_FIELDS:
        value = run_tool("dpkg-deb", ["-f", os.fspath(package), field]).stdout.strip()
        if value:
            fail(
                "LINUX_PACKAGE_SEMANTICS_INVALID",
                f"DEB control field {field} is forbidden by the fixed install contract",
            )
    for field in DEB_BOOLEAN_OR_DEFAULT_NO_FIELDS:
        value = run_tool("dpkg-deb", ["-f", os.fspath(package), field]).stdout.strip()
        if value not in {"", "no"}:
            fail(
                "LINUX_PACKAGE_SEMANTICS_INVALID",
                f"DEB control field {field} changes fixed package-manager semantics",
            )
    architecture = {"amd64": "x86_64", "arm64": "aarch64"}.get(deb_arch, deb_arch)
    if package_name != expected_name or architecture != expected_architecture or not version:
        fail("LINUX_PACKAGE_IDENTITY_INVALID", "DEB name, version, or architecture differs from the release candidate")

    tool = resolve_tool("dpkg-deb")
    entries: list[dict[str, Any]] = []
    seen: set[str] = set()
    with tempfile.TemporaryFile() as diagnostics:
        process = subprocess.Popen(
            [tool, "--fsys-tarfile", os.fspath(package)],
            stdout=subprocess.PIPE,
            stderr=diagnostics,
            env=hermetic_tool_environment(),
        )
        assert process.stdout is not None
        try:
            with tarfile.open(fileobj=process.stdout, mode="r|*") as archive:
                for member in archive:
                    # Canonical Debian data archives commonly start with an
                    # explicit `./` directory entry. It has no install path.
                    if package_path_is_archive_root(member.name):
                        continue
                    path = normalize_package_path(member.name)
                    if path in seen:
                        fail("LINUX_PACKAGE_CONTENT_INVALID", f"duplicate DEB payload path {path}")
                    seen.add(path)
                    entry_type = tar_entry_type(member)
                    digest = ""
                    if entry_type == "file":
                        source = archive.extractfile(member)
                        if source is None:
                            fail("LINUX_PACKAGE_CONTENT_INVALID", f"cannot read DEB payload {path}")
                        hasher = hashlib.sha256()
                        for chunk in iter(lambda: source.read(CHUNK_BYTES), b""):
                            hasher.update(chunk)
                        digest = hasher.hexdigest()
                    entries.append(
                        {
                            "path": path,
                            "type": entry_type,
                            "mode": f"{member.mode & 0o7777:04o}",
                            "owner": str(member.uid),
                            "group": str(member.gid),
                            "size": member.size,
                            "sha256": digest,
                            "linkTarget": member.linkname if entry_type in {"symlink", "hardlink"} else "",
                        }
                    )
        except (tarfile.TarError, OSError) as error:
            process.kill()
            process.wait()
            fail("LINUX_PACKAGE_CONTENT_INVALID", f"cannot inspect DEB payload: {error}")
        finally:
            process.stdout.close()
        return_code = process.wait()
        if return_code != 0:
            diagnostics.seek(0)
            detail = diagnostics.read(2000).decode("utf-8", "replace")
            fail("LINUX_PACKAGE_CONTENT_INVALID", f"dpkg-deb payload extraction failed: {detail}")
    entries.sort(key=lambda item: item["path"])
    scripts = inspect_deb_control_scripts(package)
    return {
        "packageName": package_name,
        "packageVersion": version,
        "architecture": architecture,
        "dependencies": parse_deb_dependencies(dependency_metadata),
        "entries": entries,
        "scripts": scripts,
    }


def rpm_file_type(mode: int) -> str:
    if stat.S_ISREG(mode):
        return "file"
    if stat.S_ISDIR(mode):
        return "directory"
    if stat.S_ISLNK(mode):
        return "symlink"
    if stat.S_ISCHR(mode):
        return "char_device"
    if stat.S_ISBLK(mode):
        return "block_device"
    if stat.S_ISFIFO(mode):
        return "fifo"
    return "other"


def rpm_query_tag(package: Path, tag: str) -> bytes:
    return run_tool_bytes(
        "rpm",
        ["--noplugins", "-qp", "--qf", f"%{{{tag}}}", os.fspath(package)],
    ).stdout


def rpm_tag_is_absent(value: bytes) -> bool:
    return value.strip() in {b"", b"(none)"}


def inspect_rpm_scripts(package: Path) -> dict[str, dict[str, Any]]:
    scripts: dict[str, dict[str, Any]] = {}
    for phase, (body_tag, interpreter_tag) in RPM_SCRIPT_QUERY_TAGS.items():
        content = rpm_query_tag(package, body_tag)
        if rpm_tag_is_absent(content):
            fail("LINUX_PACKAGE_SCRIPT_INVALID", f"RPM %{phase} scriptlet is missing")
        interpreter_bytes = rpm_query_tag(package, interpreter_tag)
        try:
            interpreter = interpreter_bytes.decode("utf-8").strip()
        except UnicodeDecodeError as error:
            fail("LINUX_PACKAGE_SCRIPT_INVALID", f"RPM %{phase} interpreter is not UTF-8: {error}")
        if interpreter != "/bin/sh":
            fail("LINUX_PACKAGE_SCRIPT_INVALID", f"RPM %{phase} interpreter must be /bin/sh")
        scripts[phase] = package_script_evidence(content, f"RPM %{phase}", interpreter)

    # Any additional transaction, verify, or trigger script would execute
    # code that is not represented by the fixed three-script policy.
    for tag in RPM_FORBIDDEN_SCRIPT_TAGS:
        if not rpm_tag_is_absent(rpm_query_tag(package, tag)):
            fail("LINUX_PACKAGE_SCRIPT_INVALID", f"RPM contains unpinned scriptlet tag {tag}")
    for tag in RPM_FORBIDDEN_PRIMARY_SCRIPT_FLAGS:
        if not rpm_tag_is_absent(rpm_query_tag(package, tag)):
            fail("LINUX_PACKAGE_SCRIPT_INVALID", f"RPM contains unsupported scriptlet flags {tag}")
    return scripts


def inspect_rpm_semantics(
    package: Path,
    package_name: str,
    version: str,
    architecture: str,
) -> None:
    for tag in RPM_FORBIDDEN_SEMANTIC_TAGS:
        if not rpm_tag_is_absent(rpm_query_tag(package, tag)):
            fail(
                "LINUX_PACKAGE_SEMANTICS_INVALID",
                f"RPM contains forbidden package-manager metadata tag {tag}",
            )

    provides_output = run_tool(
        "rpm",
        ["--noplugins", "-qp", "--provides", os.fspath(package)],
    ).stdout
    provides = {line.strip() for line in provides_output.splitlines() if line.strip()}
    base_provide = f"{package_name} = {version}"
    architecture_suffix = {"x86_64": "x86-64", "aarch64": "aarch-64"}.get(architecture)
    allowed_provides = {base_provide}
    if architecture_suffix is not None:
        allowed_provides.add(f"{package_name}({architecture_suffix}) = {version}")
    if base_provide not in provides or not provides <= allowed_provides:
        fail(
            "LINUX_PACKAGE_SEMANTICS_INVALID",
            "RPM Provides metadata differs from the fixed package self-identity",
        )


def inspect_rpm_package(package: Path, expected_name: str, expected_architecture: str) -> dict[str, Any]:
    identity = run_tool(
        "rpm",
        [
            "--noplugins",
            "-qp",
            "--qf",
            "%{NAME}\n%{VERSION}-%{RELEASE}\n%{ARCH}\n%{FILEDIGESTALGO}\n",
            os.fspath(package),
        ],
    ).stdout.splitlines()
    if len(identity) != 4:
        fail("LINUX_PACKAGE_IDENTITY_INVALID", "RPM identity query is incomplete")
    package_name, version, architecture, digest_algorithm = identity
    if package_name != expected_name or architecture != expected_architecture or not version:
        fail("LINUX_PACKAGE_IDENTITY_INVALID", "RPM name, version, or architecture differs from the release candidate")
    if digest_algorithm != "8":
        fail("LINUX_PACKAGE_CONTENT_INVALID", "RPM file digests must use SHA-256")
    dependency_metadata = run_tool(
        "rpm", ["--noplugins", "-qp", "--requires", os.fspath(package)]
    ).stdout
    query_format = (
        "[%{FILENAMES}\t%{FILEMODES:octal}\t%{FILEUSERNAME}\t%{FILEGROUPNAME}\t"
        "%{FILESIZES}\t%{FILEDIGESTS}\t%{FILELINKTOS}\t%{FILEINODES}\t%{FILEDEVICES}\t"
        "%{FILECAPS}\t%{FILEFLAGS}\n]"
    )
    output = run_tool(
        "rpm", ["--noplugins", "-qp", "--qf", query_format, os.fspath(package)]
    ).stdout
    entries: list[dict[str, Any]] = []
    seen: set[str] = set()
    hardlink_identities: dict[tuple[int, int], str] = {}
    for line in output.splitlines():
        fields = line.split("\t")
        if len(fields) != 11:
            fail("LINUX_PACKAGE_CONTENT_INVALID", "RPM file metadata query is malformed")
        (
            filename,
            mode_text,
            owner,
            group,
            size_text,
            digest,
            link_target,
            inode_text,
            device_text,
            file_capabilities,
            file_flags_text,
        ) = fields
        path = normalize_package_path(filename)
        if path in seen:
            fail("LINUX_PACKAGE_CONTENT_INVALID", f"duplicate RPM payload path {path}")
        seen.add(path)
        try:
            complete_mode = int(mode_text, 8)
            size = int(size_text)
            inode = int(inode_text)
            device = int(device_text)
            file_flags = int(file_flags_text, 10)
        except ValueError:
            fail("LINUX_PACKAGE_CONTENT_INVALID", f"RPM metadata is invalid for {path}")
        if not rpm_tag_is_absent(file_capabilities.encode("utf-8")):
            fail("LINUX_PACKAGE_CONTENT_INVALID", f"RPM payload {path} carries file capabilities")
        if file_flags != 0:
            fail(
                "LINUX_PACKAGE_SEMANTICS_INVALID",
                f"RPM payload {path} carries package-manager file flags {file_flags}",
            )
        entry_type = rpm_file_type(complete_mode)
        if entry_type == "file" and not SHA256_RE.fullmatch(digest):
            fail("LINUX_PACKAGE_CONTENT_INVALID", f"RPM file {path} has no SHA-256 digest")
        if entry_type == "file" and (inode != 0 or device != 0):
            identity = (device, inode)
            existing = hardlink_identities.get(identity)
            if existing is not None:
                fail(
                    "LINUX_PACKAGE_CONTENT_INVALID",
                    f"RPM payload hardlinks are forbidden: {existing} and {path}",
                )
            hardlink_identities[identity] = path
        entries.append(
            {
                "path": path,
                "type": entry_type,
                "mode": f"{complete_mode & 0o7777:04o}",
                "owner": owner,
                "group": group,
                "size": size,
                "sha256": digest.lower(),
                "linkTarget": link_target,
            }
        )
    entries.sort(key=lambda item: item["path"])
    scripts = inspect_rpm_scripts(package)
    inspect_rpm_semantics(package, package_name, version, architecture)
    return {
        "packageName": package_name,
        "packageVersion": version,
        "architecture": architecture,
        "dependencies": parse_rpm_dependencies(dependency_metadata),
        "entries": entries,
        "scripts": scripts,
    }


def inspect_package(kind: str, package: Path, expected_name: str, expected_architecture: str) -> dict[str, Any]:
    if kind == "deb":
        return inspect_deb_package(package, expected_name, expected_architecture)
    if kind == "rpm":
        return inspect_rpm_package(package, expected_name, expected_architecture)
    fail("LINUX_PACKAGE_KIND_INVALID", f"unsupported package kind {kind}")


def package_entry_map(package_details: dict[str, Any]) -> dict[str, dict[str, Any]]:
    entries = package_details.get("entries")
    if not isinstance(entries, list):
        fail("LINUX_PACKAGE_CONTENT_INVALID", "package entries are missing")
    result: dict[str, dict[str, Any]] = {}
    for entry in entries:
        if not isinstance(entry, dict) or not isinstance(entry.get("path"), str):
            fail("LINUX_PACKAGE_CONTENT_INVALID", "package entry is malformed")
        if entry["path"] in result:
            fail("LINUX_PACKAGE_CONTENT_INVALID", f"duplicate package path {entry['path']}")
        result[entry["path"]] = entry
    return result


def verify_package_dependencies(
    package_details: dict[str, Any],
    policy: dict[str, Any],
    kind: str,
) -> dict[str, list[str]]:
    dependencies = package_details.get("dependencies")
    if not isinstance(dependencies, dict):
        fail("LINUX_PACKAGE_DEPENDENCY_INVALID", "package dependency metadata is missing")
    require_exact_keys(
        dependencies,
        {"requirements", "directPackages"},
        f"packageDependencies.{kind}",
    )
    requirements = dependencies.get("requirements")
    direct_packages = dependencies.get("directPackages")
    if (
        not isinstance(requirements, list)
        or any(not isinstance(item, str) or not item for item in requirements)
        or not isinstance(direct_packages, list)
        or any(not isinstance(item, str) or not item for item in direct_packages)
    ):
        fail("LINUX_PACKAGE_DEPENDENCY_INVALID", "package dependency evidence is malformed")
    reparsed = (
        parse_deb_dependencies(", ".join(requirements))
        if kind == "deb"
        else parse_rpm_dependencies("\n".join(requirements))
    )
    if reparsed != dependencies:
        fail("LINUX_PACKAGE_DEPENDENCY_INVALID", "package dependency evidence is not canonical")
    required = policy["packageDependencies"][kind]
    if kind == "deb":
        expected = parse_deb_dependencies(", ".join(required))
        if dependencies != expected:
            fail(
                "LINUX_PACKAGE_DEPENDENCY_INVALID",
                "DEB Depends must exactly equal the fixed direct dependency list",
            )
    else:
        if len(requirements) != len(set(requirements)):
            fail("LINUX_PACKAGE_DEPENDENCY_INVALID", "RPM Requires contains duplicates")
        required_set = set(required)
        requirement_set = set(requirements)
        missing = sorted(required_set - requirement_set)
        if missing:
            fail(
                "LINUX_PACKAGE_DEPENDENCY_MISSING",
                f"rpm package metadata is missing direct dependencies {missing}",
            )
        if "/bin/sh" not in requirement_set:
            fail(
                "LINUX_PACKAGE_DEPENDENCY_MISSING",
                "RPM package metadata must require the pinned scriptlet interpreter /bin/sh",
            )
        unexpected = sorted(
            requirement_set - required_set - RPM_ALLOWED_AUTOGENERATED_REQUIREMENTS
        )
        if unexpected:
            fail(
                "LINUX_PACKAGE_DEPENDENCY_INVALID",
                f"RPM Requires contains unreviewed dependencies {unexpected}",
            )
    return {
        "required": list(required),
        "packageMetadata": list(requirements),
    }


def package_payload_allowed_paths(policy: dict[str, Any]) -> tuple[set[str], set[str]]:
    allowed_files = {
        *(policy["installedArtifacts"][role]["path"] for role in ROLE_NAMES),
        *policy["packagePayload"]["desktopFiles"],
        *policy["packagePayload"]["iconFiles"],
    }
    allowed_directories: set[str] = set()
    for path in allowed_files:
        parent = PurePosixPath(path).parent
        while parent != PurePosixPath("/"):
            allowed_directories.add(str(parent))
            parent = parent.parent
    return allowed_files, allowed_directories


def verify_package_payload(package_details: dict[str, Any], policy: dict[str, Any]) -> None:
    entries = package_entry_map(package_details)
    allowed_files, allowed_directories = package_payload_allowed_paths(policy)
    for path, entry in entries.items():
        require_exact_keys(
            entry,
            {"path", "type", "mode", "owner", "group", "size", "sha256", "linkTarget"},
            f"packagePayload[{path}]",
        )
        if normalize_package_path(path) != path:
            fail("LINUX_PACKAGE_CONTENT_INVALID", f"package path is not canonical: {path}")
        entry_type = string_value(entry, "type", f"packagePayload[{path}]")
        if entry_type not in {"file", "directory"}:
            fail(
                "LINUX_PACKAGE_CONTENT_INVALID",
                f"package payload {path} has forbidden type {entry_type}",
            )
        expected_type = "file" if path in allowed_files else "directory" if path in allowed_directories else None
        if expected_type is None:
            fail("LINUX_PACKAGE_CONTENT_INVALID", f"package payload path is not allowlisted: {path}")
        if entry_type != expected_type:
            fail("LINUX_PACKAGE_CONTENT_INVALID", f"package payload {path} must be a {expected_type}")
        mode_text = string_value(entry, "mode", f"packagePayload[{path}]")
        if not re.fullmatch(r"0[0-7]{3}", mode_text):
            fail("LINUX_PACKAGE_CONTENT_INVALID", f"package payload {path} has an invalid mode")
        mode = int(mode_text, 8)
        if mode & (stat.S_ISUID | stat.S_ISGID):
            fail("LINUX_PACKAGE_CONTENT_INVALID", f"package payload {path} carries setuid/setgid bits")
        if mode & (stat.S_IWGRP | stat.S_IWOTH):
            fail("LINUX_PACKAGE_CONTENT_INVALID", f"package payload {path} is group/world-writable")
        if str(entry.get("owner")) not in {"0", "root"} or str(entry.get("group")) not in {"0", "root"}:
            fail("LINUX_PACKAGE_CONTENT_INVALID", f"package payload {path} must be root:root")
        size = entry.get("size")
        if isinstance(size, bool) or not isinstance(size, int) or size < 0:
            fail("LINUX_PACKAGE_CONTENT_INVALID", f"package payload {path} has an invalid size")
        digest = entry.get("sha256")
        link_target = entry.get("linkTarget")
        if not isinstance(digest, str) or not isinstance(link_target, str) or link_target:
            fail("LINUX_PACKAGE_CONTENT_INVALID", f"package payload {path} has invalid link metadata")
        if entry_type == "file":
            if size <= 0 or not SHA256_RE.fullmatch(digest):
                fail("LINUX_PACKAGE_CONTENT_INVALID", f"package file {path} must be non-empty with a SHA-256")
        elif size != 0 or digest:
            fail("LINUX_PACKAGE_CONTENT_INVALID", f"package directory {path} carries unexpected content metadata")

    missing_files = sorted(allowed_files - set(entries))
    if missing_files:
        fail("LINUX_PACKAGE_CONTENT_INVALID", f"package payload is missing fixed files {missing_files}")


def verify_package_scripts(
    package_details: dict[str, Any],
    policy: dict[str, Any],
    kind: str,
) -> dict[str, Any]:
    scripts = package_details.get("scripts")
    if not isinstance(scripts, dict):
        fail("LINUX_PACKAGE_SCRIPT_INVALID", "package script evidence is missing")
    mapping = policy["packageScripts"]["mappings"][kind]
    if set(scripts) != set(mapping):
        fail(
            "LINUX_PACKAGE_SCRIPT_INVALID",
            f"{kind} scriptlets differ: missing={sorted(set(mapping) - set(scripts))}, "
            f"extra={sorted(set(scripts) - set(mapping))}",
        )
    verified_phases: dict[str, Any] = {}
    for phase, logical_name in mapping.items():
        evidence = scripts.get(phase)
        if not isinstance(evidence, dict):
            fail("LINUX_PACKAGE_SCRIPT_INVALID", f"{kind} {phase} script evidence is malformed")
        require_exact_keys(
            evidence,
            {"rawSha256", "normalizedSha256", "size", "interpreter"},
            f"packageScripts.{kind}.{phase}",
        )
        raw_sha256 = string_value(evidence, "rawSha256", f"packageScripts.{kind}.{phase}").lower()
        normalized_sha256 = string_value(
            evidence,
            "normalizedSha256",
            f"packageScripts.{kind}.{phase}",
        ).lower()
        size = integer_value(evidence, "size", f"packageScripts.{kind}.{phase}")
        interpreter = string_value(evidence, "interpreter", f"packageScripts.{kind}.{phase}")
        if not SHA256_RE.fullmatch(raw_sha256) or not SHA256_RE.fullmatch(normalized_sha256) or size <= 0:
            fail("LINUX_PACKAGE_SCRIPT_INVALID", f"{kind} {phase} script evidence is invalid")
        definition = policy["packageScripts"]["definitions"][logical_name]
        if raw_sha256 != definition["rawSha256"][kind].lower():
            fail("LINUX_PACKAGE_SCRIPT_MISMATCH", f"{kind} {phase} raw bytes differ from fixed {logical_name}")
        if normalized_sha256 != definition["normalizedSha256"].lower():
            fail("LINUX_PACKAGE_SCRIPT_MISMATCH", f"{kind} {phase} normalized bytes differ from fixed {logical_name}")
        if interpreter != policy["packageScripts"]["interpreter"]:
            fail("LINUX_PACKAGE_SCRIPT_MISMATCH", f"{kind} {phase} interpreter differs from fixed policy")
        verified_phases[phase] = {
            "logicalName": logical_name,
            "rawSha256": raw_sha256,
            "normalizedSha256": normalized_sha256,
            "size": size,
            "interpreter": interpreter,
        }
    return {
        "normalization": policy["packageScripts"]["normalization"],
        "phases": verified_phases,
    }


def canonical_json_sha256(value: Any) -> str:
    encoded = json.dumps(value, sort_keys=True, separators=(",", ":")).encode("utf-8")
    return hashlib.sha256(encoded).hexdigest()


def verify_package_artifact_binding(
    package_details: dict[str, Any],
    installed: dict[str, StableFile],
    policy: dict[str, Any],
) -> None:
    entries = package_entry_map(package_details)
    for role in ROLE_NAMES:
        contract = policy["installedArtifacts"][role]
        path = contract["path"]
        entry = entries.get(path)
        if entry is None:
            fail("LINUX_PACKAGE_CONTENT_INVALID", f"package does not contain fixed {role} path {path}")
        if entry.get("type") != "file" or entry.get("sha256", "").lower() != installed[role].sha256.lower():
            fail("LINUX_PACKAGE_CONTENT_MISMATCH", f"package {role} bytes differ from the installed artifact")
        if entry.get("size") != installed[role].size:
            fail("LINUX_PACKAGE_CONTENT_MISMATCH", f"package {role} size differs from the installed artifact")
        if entry.get("mode") != contract["mode"]:
            fail("LINUX_PACKAGE_CONTENT_MISMATCH", f"package {role} mode differs from fixed policy")
        if str(entry.get("owner")) not in {"0", "root"} or str(entry.get("group")) not in {"0", "root"}:
            fail("LINUX_PACKAGE_CONTENT_MISMATCH", f"package {role} is not root:root")


def ensure_directory_tree(path: Path) -> None:
    path = absolute_path(path)
    current = Path(path.anchor)
    for component in path.parts[1:]:
        current /= component
        try:
            metadata = os.lstat(current)
        except FileNotFoundError:
            try:
                os.mkdir(current, 0o755)
            except FileExistsError:
                pass
            except OSError as error:
                fail("LINUX_PACKAGE_MANIFEST_PATH_INVALID", f"cannot create output directory {current}: {error}")
            try:
                metadata = os.lstat(current)
            except OSError as error:
                fail("LINUX_PACKAGE_MANIFEST_PATH_INVALID", f"cannot inspect output directory {current}: {error}")
        if stat.S_ISLNK(metadata.st_mode) or not stat.S_ISDIR(metadata.st_mode):
            fail("LINUX_PACKAGE_MANIFEST_PATH_INVALID", f"output directory component is not a real directory: {current}")


def write_json_atomic(path: Path, value: dict[str, Any]) -> StableFile:
    path = absolute_path(path)
    ensure_directory_tree(path.parent)
    ensure_no_symlink_components(path.parent, "package manifest directory")
    try:
        existing = os.lstat(path)
    except FileNotFoundError:
        existing = None
    if existing is not None and not stat.S_ISREG(existing.st_mode):
        fail("LINUX_PACKAGE_MANIFEST_PATH_INVALID", "refusing to replace a non-regular package manifest")
    encoded = (json.dumps(value, sort_keys=True, indent=2, separators=(",", ": ")) + "\n").encode("utf-8")
    temporary_name: str | None = None
    try:
        with tempfile.NamedTemporaryFile(prefix=f".{path.name}.", dir=path.parent, delete=False) as output:
            temporary_name = output.name
            os.fchmod(output.fileno(), 0o644)
            output.write(encoded)
            output.flush()
            os.fsync(output.fileno())
        os.replace(temporary_name, path)
        temporary_name = None
        directory_fd = os.open(path.parent, os.O_RDONLY | getattr(os, "O_CLOEXEC", 0))
        try:
            os.fsync(directory_fd)
        finally:
            os.close(directory_fd)
    except OSError as error:
        fail("LINUX_PACKAGE_MANIFEST_WRITE_FAILED", f"cannot publish package manifest: {error}")
    finally:
        if temporary_name is not None:
            try:
                os.unlink(temporary_name)
            except FileNotFoundError:
                pass
    return read_stable_file(path, "generated package manifest", max_bytes=64 * 1024 * 1024)


def verify_release(
    manifest_path: Path,
    *,
    policy_path: Path,
    signature_verifier: Callable[[str, Path, Path | None, Path, str], str] | None = None,
    package_inspector: Callable[[str, Path, str, str], dict[str, Any]] = inspect_package,
    installed_inspector: Callable[[Path, str, dict[str, Any]], StableFile] = inspect_installed_artifact,
) -> dict[str, Any]:
    manifest, manifest_evidence = load_json(manifest_path, "Linux release manifest")
    policy, policy_evidence = load_json(policy_path, "Linux release policy")
    validate_policy(policy)
    validate_manifest(manifest, policy, lint=False)
    if not manifest["captureReleaseEnabled"]:
        fail("LINUX_CAPTURE_RELEASE_DISABLED", "captureReleaseEnabled is false")
    if policy["configurationState"] != "configured":
        fail("LINUX_RELEASE_POLICY_UNCONFIGURED", "fixed Linux release identity and architecture policy is unconfigured")
    git_commit = manifest["gitCommit"]
    build_id = manifest["buildId"]
    if not COMMIT_RE.fullmatch(git_commit) or not BUILD_ID_RE.fullmatch(build_id):
        fail("LINUX_BUILD_IDENTITY_INVALID", "gitCommit or buildId is invalid")

    manifest_directory = manifest_evidence.path.parent
    package_config = manifest["package"]
    signature_config = package_config["signature"]
    kind = package_config["kind"]
    package_path = resolve_manifest_file(manifest_directory, package_config["path"], "package.path")
    public_key_path = resolve_manifest_file(
        manifest_directory,
        signature_config["publicKeyPath"],
        "package.signature.publicKeyPath",
    )
    detached_signature_path = (
        resolve_manifest_file(manifest_directory, signature_config["path"], "package.signature.path")
        if kind == "deb"
        else None
    )
    expected_fingerprint = normalize_fingerprint(
        policy["packageSignatures"][kind]["signerFingerprint"],
        f"releasePolicy.packageSignatures.{kind}",
    )
    signature_evidence: StableFile | None = None
    with tempfile.TemporaryDirectory(
        prefix="taomni-linux-gate-inputs-", dir="/tmp"
    ) as snapshot_directory_text:
        snapshot_directory = Path(snapshot_directory_text)
        os.chmod(snapshot_directory, 0o700)
        package_evidence, package_snapshot = snapshot_stable_file(
            package_path,
            "release package",
            snapshot_directory,
            "package.bin",
        )
        verify_expected_hash(package_evidence, package_config["sha256"], "release package")
        public_key_evidence, public_key_snapshot = snapshot_stable_file(
            public_key_path,
            "release signing public key",
            snapshot_directory,
            "release-key.asc",
            max_bytes=MAX_POLICY_BYTES,
        )
        verify_expected_hash(
            public_key_evidence,
            signature_config["publicKeySha256"],
            "release signing public key",
        )
        detached_signature_snapshot: Path | None = None
        if detached_signature_path is not None:
            signature_evidence, detached_signature_snapshot = snapshot_stable_file(
                detached_signature_path,
                "detached package signature",
                snapshot_directory,
                "package-signature.asc",
                max_bytes=MAX_POLICY_BYTES,
            )
            verify_expected_hash(
                signature_evidence,
                signature_config["sha256"],
                "detached package signature",
            )

        key_fingerprint = openpgp_primary_fingerprint(public_key_snapshot)
        if key_fingerprint != expected_fingerprint:
            fail("LINUX_PACKAGE_SIGNER_MISMATCH", "supplied public key differs from fixed policy")
        if signature_verifier is None:
            if kind == "deb":
                assert detached_signature_snapshot is not None
                verified_fingerprint = verify_deb_signature(
                    package_snapshot,
                    detached_signature_snapshot,
                    public_key_snapshot,
                    expected_fingerprint,
                )
            else:
                verified_fingerprint = verify_rpm_signature(
                    package_snapshot,
                    public_key_snapshot,
                    expected_fingerprint,
                )
        else:
            verified_fingerprint = signature_verifier(
                kind,
                package_snapshot,
                detached_signature_snapshot,
                public_key_snapshot,
                expected_fingerprint,
            )
        if normalize_fingerprint(verified_fingerprint, "verified package signer") != expected_fingerprint:
            fail("LINUX_PACKAGE_SIGNER_MISMATCH", "verified package signer differs from fixed policy")
        package_details = package_inspector(
            kind,
            package_snapshot,
            policy["packageName"],
            manifest["architecture"],
        )

    installed: dict[str, StableFile] = {}
    for role in ROLE_NAMES:
        contract = policy["installedArtifacts"][role]
        installed[role] = installed_inspector(Path(contract["path"]), role, contract)
    helper_policy_content = installed["helperPolicy"].content
    polkit_content = installed["polkitAction"].content
    if helper_policy_content is None or polkit_content is None:
        fail("LINUX_ARTIFACT_UNREADABLE", "installed policy artifacts were not captured")
    verify_helper_policy(
        helper_policy_content,
        policy["helperPolicy"],
        installed["application"].sha256,
        installed["helper"].sha256,
    )
    verify_polkit_action(polkit_content, policy["polkit"])

    verified_package_dependencies = verify_package_dependencies(package_details, policy, kind)
    verify_package_payload(package_details, policy)
    verify_package_artifact_binding(package_details, installed, policy)
    verified_package_scripts = verify_package_scripts(package_details, policy, kind)
    package_scripts_sha256 = canonical_json_sha256(verified_package_scripts)

    # Snapshot use prevents mixed A/B verification. Bind the receipt paths back
    # to the exact source inodes and metadata that produced those snapshots.
    verify_source_unchanged(package_path, package_evidence, "release package")
    verify_source_unchanged(public_key_path, public_key_evidence, "signing public key")
    if detached_signature_path is not None and signature_evidence is not None:
        verify_source_unchanged(
            detached_signature_path,
            signature_evidence,
            "detached signature",
        )
    verify_source_unchanged(manifest_evidence.path, manifest_evidence, "release manifest")
    verify_source_unchanged(policy_evidence.path, policy_evidence, "release policy")
    for role in ROLE_NAMES:
        rechecked = installed_inspector(Path(policy["installedArtifacts"][role]["path"]), role, policy["installedArtifacts"][role])
        if rechecked.sha256 != installed[role].sha256:
            fail("LINUX_ARTIFACT_CHANGED", f"installed {role} changed during release verification")

    package_manifest = {
        "schemaVersion": PACKAGE_MANIFEST_SCHEMA_VERSION,
        "manifestKind": "sockscap_linux_package",
        "platform": PLATFORM,
        "architecture": manifest["architecture"],
        "captureProvider": manifest["captureProvider"],
        "packageKind": kind,
        "packageName": package_details["packageName"],
        "packageVersion": package_details["packageVersion"],
        "packageSha256": package_evidence.sha256,
        "signatureScheme": signature_config["scheme"],
        "signerFingerprint": expected_fingerprint,
        "signatureSha256": (
            signature_evidence.sha256 if signature_evidence is not None else package_evidence.sha256
        ),
        "signingPublicKeySha256": public_key_evidence.sha256,
        "packageDependencies": verified_package_dependencies,
        "entries": package_details["entries"],
        "packageScripts": verified_package_scripts,
        "packageScriptsSha256": package_scripts_sha256,
        "verificationScope": "same_host_installed_fixed_paths",
        "externalEvidenceRequired": ["native_install_lab_provenance"],
    }
    package_manifest_path = resolve_manifest_file(
        manifest_directory,
        manifest["packageManifestOutput"],
        "packageManifestOutput",
    )
    protected_inputs = {manifest_evidence.path, policy_evidence.path, package_path, public_key_path}
    if detached_signature_path is not None:
        protected_inputs.add(detached_signature_path)
    if package_manifest_path in protected_inputs:
        fail("LINUX_PACKAGE_MANIFEST_PATH_INVALID", "package manifest output collides with a verifier input")
    package_manifest_evidence = write_json_atomic(package_manifest_path, package_manifest)

    receipt: dict[str, Any] = {
        "gateSchemaVersion": RECEIPT_SCHEMA_VERSION,
        "gateKind": "sockscap_linux_artifact",
        "platform": PLATFORM,
        "mode": "release",
        "result": "PASS",
        "architecture": manifest["architecture"],
        "captureProvider": manifest["captureProvider"],
        "capturePackageKinds": policy["capturePackageKinds"],
        "appImageCaptureDisabled": policy["appImageCaptureDisabled"],
        "verificationScope": "same_host_installed_fixed_paths",
        "externalEvidenceRequired": ["native_install_lab_provenance"],
        "gitCommit": git_commit.lower(),
        "buildId": build_id,
        "releasePolicySchemaVersion": POLICY_SCHEMA_VERSION,
        "releasePolicySha256": policy_evidence.sha256,
        "artifactManifestPath": os.fspath(manifest_evidence.path),
        "artifactManifestSha256": manifest_evidence.sha256,
        "packageKind": kind,
        "packageVersion": package_details["packageVersion"],
        "packagePath": os.fspath(package_path),
        "packageSha256": package_evidence.sha256,
        "packageSignatureScheme": signature_config["scheme"],
        "packageSignerFingerprint": expected_fingerprint,
        # RPM carries its OpenPGP signature inside the package, so its
        # signature artifact is intentionally the exact package file.
        "signaturePath": os.fspath(detached_signature_path or package_path),
        "signatureSha256": (
            signature_evidence.sha256 if signature_evidence is not None else package_evidence.sha256
        ),
        "packageSigningPublicKey": os.fspath(public_key_path),
        "packageSigningPublicKeySha256": public_key_evidence.sha256,
        "packageDependencies": verified_package_dependencies,
        "packageManifestPath": os.fspath(package_manifest_path),
        "packageManifestSha256": package_manifest_evidence.sha256,
        "packageScriptsSha256": package_scripts_sha256,
        "application": os.fspath(installed["application"].path),
        "applicationSha256": installed["application"].sha256,
        "helper": os.fspath(installed["helper"].path),
        "helperSha256": installed["helper"].sha256,
        "helperPolicy": os.fspath(installed["helperPolicy"].path),
        "helperPolicySha256": installed["helperPolicy"].sha256,
        "polkitAction": os.fspath(installed["polkitAction"].path),
        "polkitActionSha256": installed["polkitAction"].sha256,
        "packageSignatureVerified": True,
        "packagePayloadVerified": True,
        "packageDependenciesVerified": True,
        "packageScriptsVerified": True,
        "helperOwnershipVerified": True,
        "helperPolicyVerified": True,
        "polkitActionVerified": True,
    }
    return receipt


def lint_manifest(manifest_path: Path, *, policy_path: Path) -> dict[str, Any]:
    manifest, manifest_evidence = load_json(manifest_path, "Linux release manifest")
    policy, policy_evidence = load_json(policy_path, "Linux release policy")
    validate_policy(policy)
    validate_manifest(manifest, policy, lint=True)
    return {
        "gateSchemaVersion": RECEIPT_SCHEMA_VERSION,
        "gateKind": "sockscap_linux_artifact",
        "platform": PLATFORM,
        "mode": "lint",
        "captureReleaseEnabled": manifest["captureReleaseEnabled"],
        "capturePackageKinds": policy["capturePackageKinds"],
        "appImageCaptureDisabled": policy["appImageCaptureDisabled"],
        "releasePolicySchemaVersion": POLICY_SCHEMA_VERSION,
        "releasePolicyConfigurationState": policy["configurationState"],
        "releasePolicySha256": policy_evidence.sha256,
        "artifactManifestPath": os.fspath(manifest_evidence.path),
        "artifactManifestSha256": manifest_evidence.sha256,
        "result": "PASS",
    }


def committed_policy_path() -> Path:
    return Path(__file__).resolve().parents[2] / "src-tauri" / "platform" / "sockscap" / "linux" / "release-policy.json"


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--lint", action="store_true", help="validate source contracts only")
    parser.add_argument("manifest", type=Path, help="Linux release manifest")
    args = parser.parse_args(argv)
    try:
        if args.lint:
            receipt = lint_manifest(args.manifest, policy_path=committed_policy_path())
        else:
            receipt = verify_release(args.manifest, policy_path=committed_policy_path())
    except LinuxGateError as error:
        print(f"{error.code}: {error.message}", file=sys.stderr)
        return 1
    print(json.dumps(receipt, sort_keys=True, separators=(",", ":")))
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
