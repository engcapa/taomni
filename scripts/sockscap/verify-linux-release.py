class LinuxGateError(RuntimeError):
    def __init__(self, code: str, message: str):
        super().__init__(f"{code}: {message}")
        self.code = code
        self.message = message


def fail(code: str, message: str) -> None:
    raise LinuxGateError(code, message)


@dataclass(frozen=True)
class StableFile:
    path: Path
    sha256: str
    size: int
    uid: int
    gid: int
    mode: int
    content: bytes | None = None
    file_capabilities: bytes | None = None
    device: int | None = None
    inode: int | None = None
    mtime_ns: int | None = None
    ctime_ns: int | None = None


def _same_file_identity(left: os.stat_result, right: os.stat_result) -> bool:
    return (
        left.st_dev,
        left.st_ino,
        left.st_size,
        left.st_mode,
        left.st_uid,
        left.st_gid,
        left.st_mtime_ns,
        left.st_ctime_ns,
    ) == (
        right.st_dev,
        right.st_ino,
        right.st_size,
        right.st_mode,
        right.st_uid,
        right.st_gid,
        right.st_mtime_ns,
        right.st_ctime_ns,
    )


def absolute_path(path: Path) -> Path:
    return Path(os.path.abspath(os.fspath(path)))


def ensure_no_symlink_components(path: Path, label: str) -> None:
    path = absolute_path(path)
    current = Path(path.anchor)
    for component in path.parts[1:]:
        current /= component
        try:
            metadata = os.lstat(current)
        except OSError as error:
            fail("LINUX_ARTIFACT_MISSING", f"cannot inspect {label} {current}: {error}")
        if stat.S_ISLNK(metadata.st_mode):
            fail("LINUX_ARTIFACT_SYMLINK", f"{label} contains symlink component {current}")


def read_stable_file(
    path: Path,
    label: str,
    *,
    capture_content: bool = False,
    capture_file_capabilities: bool = False,
    max_bytes: int | None = None,
) -> StableFile:
    path = absolute_path(path)
    ensure_no_symlink_components(path, label)
    flags = os.O_RDONLY | getattr(os, "O_CLOEXEC", 0) | getattr(os, "O_NOFOLLOW", 0)
    try:
        descriptor = os.open(path, flags)
    except OSError as error:
        fail("LINUX_ARTIFACT_UNREADABLE", f"cannot open {label} {path}: {error}")
    try:
        before = os.fstat(descriptor)
        if not stat.S_ISREG(before.st_mode) or before.st_size <= 0:
            fail("LINUX_ARTIFACT_TYPE_INVALID", f"{label} must be a non-empty regular file")
        if max_bytes is not None and before.st_size > max_bytes:
            fail("LINUX_ARTIFACT_SIZE_INVALID", f"{label} exceeds {max_bytes} bytes")
        digest = hashlib.sha256()
        captured = bytearray() if capture_content else None
        while True:
            try:
                chunk = os.read(descriptor, CHUNK_BYTES)
            except OSError as error:
                fail("LINUX_ARTIFACT_UNREADABLE", f"cannot read {label}: {error}")
            if not chunk:
                break
            digest.update(chunk)
            if captured is not None:
                captured.extend(chunk)
        capabilities = (
            read_file_capabilities(descriptor) if capture_file_capabilities else None
        )
        after = os.fstat(descriptor)
        if not _same_file_identity(before, after):
            fail("LINUX_ARTIFACT_CHANGED", f"{label} changed while it was being hashed")
        return StableFile(
            path=path,
            sha256=digest.hexdigest(),
            size=before.st_size,
            uid=before.st_uid,
            gid=before.st_gid,
            mode=stat.S_IMODE(before.st_mode),
            content=bytes(captured) if captured is not None else None,
            file_capabilities=capabilities,
            device=before.st_dev,
            inode=before.st_ino,
            mtime_ns=before.st_mtime_ns,
            ctime_ns=before.st_ctime_ns,
        )
    finally:
        os.close(descriptor)


def snapshot_stable_file(
    path: Path,
    label: str,
    snapshot_directory: Path,
    snapshot_name: str,
    *,
    max_bytes: int | None = None,
) -> tuple[StableFile, Path]:
    """Copy one stable source fd into a private verifier-owned snapshot."""
    path = absolute_path(path)
    ensure_no_symlink_components(path, label)
    source_flags = os.O_RDONLY | getattr(os, "O_CLOEXEC", 0) | getattr(os, "O_NOFOLLOW", 0)
    try:
        source = os.open(path, source_flags)
    except OSError as error:
        fail("LINUX_ARTIFACT_UNREADABLE", f"cannot open {label} {path}: {error}")
    snapshot_path = snapshot_directory / snapshot_name
    output = -1
    try:
        before = os.fstat(source)
        if not stat.S_ISREG(before.st_mode) or before.st_size <= 0:
            fail("LINUX_ARTIFACT_TYPE_INVALID", f"{label} must be a non-empty regular file")
        if max_bytes is not None and before.st_size > max_bytes:
            fail("LINUX_ARTIFACT_SIZE_INVALID", f"{label} exceeds {max_bytes} bytes")
        output = os.open(
            snapshot_path,
            os.O_WRONLY | os.O_CREAT | os.O_EXCL | getattr(os, "O_CLOEXEC", 0),
            0o600,
        )
        os.fchmod(output, 0o600)
        digest = hashlib.sha256()
        copied = 0
        while True:
            chunk = os.read(source, CHUNK_BYTES)
            if not chunk:
                break
            digest.update(chunk)
            copied += len(chunk)
            pending = memoryview(chunk)
            while pending:
                written = os.write(output, pending)
                if written <= 0:
                    fail("LINUX_ARTIFACT_UNREADABLE", f"cannot snapshot {label}")
                pending = pending[written:]
        os.fsync(output)
        after = os.fstat(source)
        if not _same_file_identity(before, after) or copied != before.st_size:
            fail("LINUX_ARTIFACT_CHANGED", f"{label} changed while it was snapshotted")
        snapshot_metadata = os.fstat(output)
        if snapshot_metadata.st_size != before.st_size:
            fail("LINUX_ARTIFACT_CHANGED", f"{label} snapshot size is incomplete")
        return (
            StableFile(
                path=path,
                sha256=digest.hexdigest(),
                size=before.st_size,
                uid=before.st_uid,
                gid=before.st_gid,
                mode=stat.S_IMODE(before.st_mode),
                device=before.st_dev,
                inode=before.st_ino,
                mtime_ns=before.st_mtime_ns,
                ctime_ns=before.st_ctime_ns,
            ),
            snapshot_path,
        )
    except LinuxGateError:
        raise
    except OSError as error:
        fail("LINUX_ARTIFACT_UNREADABLE", f"cannot snapshot {label}: {error}")
    finally:
        if output >= 0:
            os.close(output)
        os.close(source)
        if sys.exc_info()[0] is not None:
            try:
                os.unlink(snapshot_path)
            except FileNotFoundError:
                pass


def verify_source_unchanged(path: Path, original: StableFile, label: str) -> None:
    """Prove that a receipt path still names the source fd that was snapshotted."""
    current = read_stable_file(path, f"{label} source recheck")
    original_identity = (
        original.device,
        original.inode,
        original.size,
        original.uid,
        original.gid,
        original.mode,
        original.mtime_ns,
        original.ctime_ns,
    )
    current_identity = (
        current.device,
        current.inode,
        current.size,
        current.uid,
        current.gid,
        current.mode,
        current.mtime_ns,
        current.ctime_ns,
    )
    if None in original_identity or current_identity != original_identity:
        fail("LINUX_ARTIFACT_CHANGED", f"{label} path changed during release verification")
    if current.sha256 != original.sha256:
        fail("LINUX_ARTIFACT_CHANGED", f"{label} bytes changed during release verification")


def _reject_duplicate_json_keys(pairs: list[tuple[str, Any]]) -> dict[str, Any]:
    value: dict[str, Any] = {}
    for key, item in pairs:
        if key in value:
            fail("LINUX_JSON_DUPLICATE_KEY", f"duplicate JSON key {key!r}")
        value[key] = item
    return value


def decode_json(content: bytes, label: str) -> dict[str, Any]:
    try:
        value = json.loads(content, object_pairs_hook=_reject_duplicate_json_keys)
    except LinuxGateError:
        raise
    except (UnicodeDecodeError, json.JSONDecodeError) as error:
        fail("LINUX_JSON_INVALID", f"{label} is not valid UTF-8 JSON: {error}")
    if not isinstance(value, dict):
        fail("LINUX_JSON_INVALID", f"{label} must contain one JSON object")
    return value


def load_json(path: Path, label: str) -> tuple[dict[str, Any], StableFile]:
    evidence = read_stable_file(
        path,
        label,
        capture_content=True,
        max_bytes=MAX_JSON_BYTES,
    )
    assert evidence.content is not None
    return decode_json(evidence.content, label), evidence


def require_exact_keys(value: dict[str, Any], expected: Iterable[str], label: str) -> None:
    expected_set = set(expected)
    actual = set(value)
    if actual != expected_set:
        fail(
            "LINUX_SCHEMA_INVALID",
            f"{label} keys differ: missing={sorted(expected_set - actual)}, extra={sorted(actual - expected_set)}",
        )


def object_value(value: dict[str, Any], key: str, label: str) -> dict[str, Any]:
    item = value.get(key)
    if not isinstance(item, dict):
        fail("LINUX_SCHEMA_INVALID", f"{label}.{key} must be an object")
    return item


def string_value(value: dict[str, Any], key: str, label: str) -> str:
    item = value.get(key)
    if not isinstance(item, str):
        fail("LINUX_SCHEMA_INVALID", f"{label}.{key} must be a string")
    return item


def integer_value(value: dict[str, Any], key: str, label: str) -> int:
    item = value.get(key)
    if isinstance(item, bool) or not isinstance(item, int):
        fail("LINUX_SCHEMA_INVALID", f"{label}.{key} must be an integer")
    return item


def boolean_value(value: dict[str, Any], key: str, label: str) -> bool:
    item = value.get(key)
    if not isinstance(item, bool):
        fail("LINUX_SCHEMA_INVALID", f"{label}.{key} must be a boolean")
    return item


def parse_mode(value: str, label: str) -> int:
    if not re.fullmatch(r"0[0-7]{3}", value):
        fail("LINUX_RELEASE_POLICY_INVALID", f"{label} must be a four-digit octal mode")
    return int(value, 8)


def normalize_fingerprint(value: str, label: str, *, allow_unconfigured: bool = False) -> str:
    if allow_unconfigured and value == "UNCONFIGURED":
        return value
    if not FINGERPRINT_RE.fullmatch(value):
        fail(
            "LINUX_SIGNER_FINGERPRINT_INVALID",
            f"{label} must be a complete 40- or 64-hex OpenPGP fingerprint",
        )
    normalized = value.upper()
    if set(normalized) == {"0"}:
        fail("LINUX_SIGNER_FINGERPRINT_INVALID", f"{label} cannot be all zeroes")
    return normalized


def validate_policy_sha256(value: Any, label: str) -> str:
    if not isinstance(value, str):
        fail("LINUX_RELEASE_POLICY_INVALID", f"{label} must be a SHA-256 string")
    if not SHA256_RE.fullmatch(value) or set(value.lower()) == {"0"}:
        fail("LINUX_RELEASE_POLICY_INVALID", f"fixed policy must pin {label} as a nonzero SHA-256")
    return value.lower()


def validate_policy(policy: dict[str, Any]) -> None:
    require_exact_keys(
        policy,
        {
            "schemaVersion",
            "configurationState",
            "platform",
            "captureProvider",
            "packageName",
            "capturePackageKinds",
            "packageDependencyContractState",
            "packageDependencies",
            "appImageCaptureDisabled",
            "supportedArchitectures",
            "installedArtifacts",
            "helperPolicy",
            "polkit",
            "packagePayload",
            "packageSignatures",
            "packageScripts",
        },
        "releasePolicy",
    )
    if integer_value(policy, "schemaVersion", "releasePolicy") != POLICY_SCHEMA_VERSION:
        fail("LINUX_RELEASE_POLICY_INVALID", "unsupported release policy schemaVersion")
    state = string_value(policy, "configurationState", "releasePolicy")
    if state not in {"unconfigured", "configured"}:
        fail("LINUX_RELEASE_POLICY_INVALID", "configurationState must be configured or unconfigured")
    if string_value(policy, "platform", "releasePolicy") != PLATFORM:
        fail("LINUX_RELEASE_POLICY_INVALID", "release policy platform must be linux")
    if string_value(policy, "captureProvider", "releasePolicy") != CAPTURE_PROVIDER:
        fail("LINUX_RELEASE_POLICY_INVALID", "release policy provider is unsupported")
    package_name = string_value(policy, "packageName", "releasePolicy")
    if not re.fullmatch(r"[a-z0-9][a-z0-9+.-]{0,127}", package_name):
        fail("LINUX_RELEASE_POLICY_INVALID", "packageName is invalid")

    capture_package_kinds = policy.get("capturePackageKinds")
    if capture_package_kinds != list(CAPTURE_PACKAGE_KINDS):
        fail("LINUX_RELEASE_POLICY_INVALID", "capturePackageKinds must be exactly [deb, rpm]")
    dependency_contract_state = string_value(
        policy,
        "packageDependencyContractState",
        "releasePolicy",
    )
    if dependency_contract_state not in {"unconfigured", "configured"}:
        fail(
            "LINUX_RELEASE_POLICY_INVALID",
            "packageDependencyContractState must be configured or unconfigured",
        )
    if state == "configured" and dependency_contract_state != "configured":
        fail(
            "LINUX_RELEASE_POLICY_INVALID",
            "configured releases require a frozen distro/architecture dependency contract",
        )
    package_dependencies = object_value(policy, "packageDependencies", "releasePolicy")
    require_exact_keys(package_dependencies, PACKAGE_KINDS, "releasePolicy.packageDependencies")
    for kind, expected in PACKAGE_DEPENDENCIES.items():
        if package_dependencies.get(kind) != list(expected):
            fail(
                "LINUX_RELEASE_POLICY_INVALID",
                f"{kind} packageDependencies must be exactly {list(expected)}",
            )
    if not boolean_value(policy, "appImageCaptureDisabled", "releasePolicy"):
        fail("LINUX_RELEASE_POLICY_INVALID", "ordinary AppImage/updater capture must remain disabled")

    architectures = policy.get("supportedArchitectures")
    if not isinstance(architectures, list) or any(not isinstance(item, str) for item in architectures):
        fail("LINUX_RELEASE_POLICY_INVALID", "supportedArchitectures must be a string array")
    if len(architectures) != len(set(architectures)) or not set(architectures) <= SUPPORTED_ARCHITECTURES:
        fail("LINUX_RELEASE_POLICY_INVALID", "supportedArchitectures contains duplicates or unsupported values")

    artifacts = object_value(policy, "installedArtifacts", "releasePolicy")
    require_exact_keys(artifacts, ROLE_NAMES, "releasePolicy.installedArtifacts")
    paths: list[str] = []
    for role in ROLE_NAMES:
        contract = object_value(artifacts, role, "releasePolicy.installedArtifacts")
        expected_keys = {"path", "uid", "gid", "mode"}
        if role == "helper":
            expected_keys.add("requireNoFileCapabilities")
        require_exact_keys(contract, expected_keys, f"releasePolicy.installedArtifacts.{role}")
        path = string_value(contract, "path", f"releasePolicy.installedArtifacts.{role}")
        if not path.startswith("/") or "\x00" in path or ".." in PurePosixPath(path).parts:
            fail("LINUX_RELEASE_POLICY_INVALID", f"{role} path must be a fixed absolute path")
        if integer_value(contract, "uid", f"releasePolicy.installedArtifacts.{role}") != 0:
            fail("LINUX_RELEASE_POLICY_INVALID", f"{role} must be owned by uid 0")
        if integer_value(contract, "gid", f"releasePolicy.installedArtifacts.{role}") != 0:
            fail("LINUX_RELEASE_POLICY_INVALID", f"{role} must be owned by gid 0")
        parse_mode(string_value(contract, "mode", f"releasePolicy.installedArtifacts.{role}"), role)
        if role == "helper" and not boolean_value(contract, "requireNoFileCapabilities", f"releasePolicy.installedArtifacts.{role}"):
            fail("LINUX_RELEASE_POLICY_INVALID", "helper must forbid file capabilities")
        paths.append(path)
    if len(paths) != len(set(paths)):
        fail("LINUX_RELEASE_POLICY_INVALID", "installed artifact paths must be unique")

    helper_policy = object_value(policy, "helperPolicy", "releasePolicy")
    require_exact_keys(helper_policy, {"schemaVersion", "productId", "pinMode"}, "releasePolicy.helperPolicy")
    if integer_value(helper_policy, "schemaVersion", "releasePolicy.helperPolicy") != 1:
        fail("LINUX_RELEASE_POLICY_INVALID", "helper policy schemaVersion must be 1")
    if string_value(helper_policy, "productId", "releasePolicy.helperPolicy") != "com.taomni.app":
        fail("LINUX_RELEASE_POLICY_INVALID", "helper policy productId is invalid")
    if string_value(helper_policy, "pinMode", "releasePolicy.helperPolicy") != "exact_installed_only":
        fail("LINUX_RELEASE_POLICY_INVALID", "helper policy pinMode must be exact_installed_only")

    polkit = object_value(policy, "polkit", "releasePolicy")
    require_exact_keys(
        polkit,
        {"actionId", "execPath", "allowAny", "allowInactive", "allowActive", "retainAuthorization"},
        "releasePolicy.polkit",
    )
    if string_value(polkit, "actionId", "releasePolicy.polkit") != "com.taomni.sockscap.helper":
        fail("LINUX_RELEASE_POLICY_INVALID", "polkit actionId is invalid")
    if string_value(polkit, "execPath", "releasePolicy.polkit") != artifacts["helper"]["path"]:
        fail("LINUX_RELEASE_POLICY_INVALID", "polkit execPath must equal the fixed helper path")
    expected_defaults = {"allowAny": "no", "allowInactive": "no", "allowActive": "auth_admin"}
    for key, expected in expected_defaults.items():
        if string_value(polkit, key, "releasePolicy.polkit") != expected:
            fail("LINUX_RELEASE_POLICY_INVALID", f"polkit {key} must be {expected}")
    if boolean_value(polkit, "retainAuthorization", "releasePolicy.polkit"):
        fail("LINUX_RELEASE_POLICY_INVALID", "polkit retained authorization is forbidden")

    package_payload = object_value(policy, "packagePayload", "releasePolicy")
    require_exact_keys(
        package_payload,
        {"desktopFiles", "iconFiles"},
        "releasePolicy.packagePayload",
    )
    if package_payload.get("desktopFiles") != list(PACKAGE_DESKTOP_FILES):
        fail("LINUX_RELEASE_POLICY_INVALID", "desktopFiles differ from the fixed Taomni package contract")
    if package_payload.get("iconFiles") != list(PACKAGE_ICON_FILES):
        fail("LINUX_RELEASE_POLICY_INVALID", "iconFiles differ from the fixed Taomni package contract")

    signatures = object_value(policy, "packageSignatures", "releasePolicy")
    require_exact_keys(signatures, PACKAGE_KINDS, "releasePolicy.packageSignatures")
    expected_schemes = {"deb": "detached_openpgp", "rpm": "rpm_embedded_openpgp"}
    for kind, scheme in expected_schemes.items():
        signature = object_value(signatures, kind, "releasePolicy.packageSignatures")
        require_exact_keys(signature, {"scheme", "signerFingerprint"}, f"releasePolicy.packageSignatures.{kind}")
        if string_value(signature, "scheme", f"releasePolicy.packageSignatures.{kind}") != scheme:
            fail("LINUX_RELEASE_POLICY_INVALID", f"{kind} signature scheme must be {scheme}")
        fingerprint = string_value(signature, "signerFingerprint", f"releasePolicy.packageSignatures.{kind}")
        normalize_fingerprint(fingerprint, f"releasePolicy.packageSignatures.{kind}", allow_unconfigured=state == "unconfigured")

    package_scripts = object_value(policy, "packageScripts", "releasePolicy")
    require_exact_keys(
        package_scripts,
        {"normalization", "interpreter", "definitions", "mappings"},
        "releasePolicy.packageScripts",
    )
    if string_value(package_scripts, "normalization", "releasePolicy.packageScripts") != PACKAGE_SCRIPT_NORMALIZATION:
        fail("LINUX_RELEASE_POLICY_INVALID", "package script normalization contract is unsupported")
    if string_value(package_scripts, "interpreter", "releasePolicy.packageScripts") != "/bin/sh":
        fail("LINUX_RELEASE_POLICY_INVALID", "package scripts must use /bin/sh")
    definitions = object_value(package_scripts, "definitions", "releasePolicy.packageScripts")
    require_exact_keys(definitions, PACKAGE_SCRIPT_NAMES, "releasePolicy.packageScripts.definitions")
    for logical_name in PACKAGE_SCRIPT_NAMES:
        definition = object_value(definitions, logical_name, "releasePolicy.packageScripts.definitions")
        require_exact_keys(
            definition,
            {"normalizedSha256", "rawSha256"},
            f"releasePolicy.packageScripts.definitions.{logical_name}",
        )
        validate_policy_sha256(
            definition.get("normalizedSha256"),
            f"packageScripts.definitions.{logical_name}.normalizedSha256",
        )
        raw_hashes = object_value(
            definition,
            "rawSha256",
            f"releasePolicy.packageScripts.definitions.{logical_name}",
        )
        require_exact_keys(
            raw_hashes,
            PACKAGE_KINDS,
            f"releasePolicy.packageScripts.definitions.{logical_name}.rawSha256",
        )
        for kind in CAPTURE_PACKAGE_KINDS:
            validate_policy_sha256(
                raw_hashes.get(kind),
                f"packageScripts.definitions.{logical_name}.rawSha256.{kind}",
            )
    mappings = object_value(package_scripts, "mappings", "releasePolicy.packageScripts")
    require_exact_keys(mappings, PACKAGE_KINDS, "releasePolicy.packageScripts.mappings")
    for kind in CAPTURE_PACKAGE_KINDS:
        mapping = object_value(mappings, kind, "releasePolicy.packageScripts.mappings")
        if mapping != PACKAGE_SCRIPT_MAPPINGS[kind]:
            fail("LINUX_RELEASE_POLICY_INVALID", f"{kind} package script mapping differs from the fixed contract")

    if state == "unconfigured":
        if architectures:
            fail("LINUX_RELEASE_POLICY_INVALID", "unconfigured policy must not claim supported architectures")
        if any(signatures[kind]["signerFingerprint"] != "UNCONFIGURED" for kind in PACKAGE_KINDS):
            fail("LINUX_RELEASE_POLICY_INVALID", "unconfigured policy must not pin a partial signing identity")
    else:
        if not architectures:
            fail("LINUX_RELEASE_POLICY_INVALID", "configured policy requires a non-empty architecture set")
        for kind in PACKAGE_KINDS:
            normalize_fingerprint(signatures[kind]["signerFingerprint"], f"releasePolicy.packageSignatures.{kind}")


def validate_safe_relative_path(value: str, label: str, *, allow_empty: bool = False) -> None:
    if allow_empty and value == "":
        return
    path = PurePosixPath(value)
    if not value or path.is_absolute() or ".." in path.parts or "\x00" in value:
        fail("LINUX_MANIFEST_PATH_INVALID", f"{label} must stay inside the manifest directory")


def validate_manifest(manifest: dict[str, Any], policy: dict[str, Any], *, lint: bool) -> None:
    require_exact_keys(
        manifest,
        {
            "schemaVersion",
            "captureReleaseEnabled",
            "gitCommit",
            "buildId",
            "platform",
            "architecture",
            "captureProvider",
            "package",
            "installedArtifacts",
            "packageManifestOutput",
        },
        "manifest",
    )
    if integer_value(manifest, "schemaVersion", "manifest") != MANIFEST_SCHEMA_VERSION:
        fail("LINUX_MANIFEST_INVALID", "unsupported manifest schemaVersion")
    boolean_value(manifest, "captureReleaseEnabled", "manifest")
    string_value(manifest, "gitCommit", "manifest")
    string_value(manifest, "buildId", "manifest")
    if string_value(manifest, "platform", "manifest") != PLATFORM:
        fail("LINUX_MANIFEST_INVALID", "manifest platform must be linux")
    architecture = string_value(manifest, "architecture", "manifest")
    if architecture not in SUPPORTED_ARCHITECTURES:
        fail("LINUX_MANIFEST_INVALID", "manifest architecture is unsupported")
    if string_value(manifest, "captureProvider", "manifest") != policy["captureProvider"]:
        fail("LINUX_RELEASE_POLICY_MISMATCH", "manifest provider differs from fixed policy")
    if policy["configurationState"] == "configured" and architecture not in policy["supportedArchitectures"]:
        fail("LINUX_RELEASE_POLICY_MISMATCH", "manifest architecture is not in the fixed support set")

    installed = object_value(manifest, "installedArtifacts", "manifest")
    require_exact_keys(installed, ROLE_NAMES, "manifest.installedArtifacts")
    for role in ROLE_NAMES:
        if string_value(installed, role, "manifest.installedArtifacts") != policy["installedArtifacts"][role]["path"]:
            fail("LINUX_RELEASE_POLICY_MISMATCH", f"manifest {role} path differs from fixed policy")

    package = object_value(manifest, "package", "manifest")
    require_exact_keys(package, {"kind", "path", "sha256", "signature"}, "manifest.package")
    kind = string_value(package, "kind", "manifest.package")
    if kind not in PACKAGE_KINDS:
        fail(
            "LINUX_PACKAGE_KIND_INVALID",
            "capture package kind must be deb or rpm; AppImage/updater capture is disabled",
        )
    if kind not in policy["capturePackageKinds"] or not policy["appImageCaptureDisabled"]:
        fail("LINUX_RELEASE_POLICY_MISMATCH", "manifest violates the fixed capture packaging contract")
    validate_safe_relative_path(string_value(package, "path", "manifest.package"), "package.path")
    package_sha256 = string_value(package, "sha256", "manifest.package")
    if not lint and not SHA256_RE.fullmatch(package_sha256):
        fail("LINUX_MANIFEST_INVALID", "package.sha256 must be a SHA-256 in release mode")
    if lint and package_sha256 and not SHA256_RE.fullmatch(package_sha256):
        fail("LINUX_MANIFEST_INVALID", "non-empty package.sha256 is invalid")

    signature = object_value(package, "signature", "manifest.package")
    require_exact_keys(
        signature,
        {"scheme", "path", "sha256", "publicKeyPath", "publicKeySha256"},
        "manifest.package.signature",
    )
    expected_scheme = policy["packageSignatures"][kind]["scheme"]
    if string_value(signature, "scheme", "manifest.package.signature") != expected_scheme:
        fail("LINUX_RELEASE_POLICY_MISMATCH", "signature scheme differs from fixed policy")
    signature_path = string_value(signature, "path", "manifest.package.signature")
    signature_sha256 = string_value(signature, "sha256", "manifest.package.signature")
    if kind == "deb":
        validate_safe_relative_path(signature_path, "package.signature.path")
        if not lint and not SHA256_RE.fullmatch(signature_sha256):
            fail("LINUX_MANIFEST_INVALID", "detached signature SHA-256 is required")
        if lint and signature_sha256 and not SHA256_RE.fullmatch(signature_sha256):
            fail("LINUX_MANIFEST_INVALID", "detached signature SHA-256 is invalid")
    elif signature_path or signature_sha256:
        fail("LINUX_MANIFEST_INVALID", "RPM embedded signatures must not declare a detached signature")
    public_key_path = string_value(signature, "publicKeyPath", "manifest.package.signature")
    validate_safe_relative_path(public_key_path, "package.signature.publicKeyPath")
    public_key_sha256 = string_value(signature, "publicKeySha256", "manifest.package.signature")
    if not lint and not SHA256_RE.fullmatch(public_key_sha256):
        fail("LINUX_MANIFEST_INVALID", "signing public key SHA-256 is required")
    if lint and public_key_sha256 and not SHA256_RE.fullmatch(public_key_sha256):
        fail("LINUX_MANIFEST_INVALID", "signing public key SHA-256 is invalid")
    validate_safe_relative_path(string_value(manifest, "packageManifestOutput", "manifest"), "packageManifestOutput")


def resolve_manifest_file(manifest_directory: Path, value: str, label: str) -> Path:
    validate_safe_relative_path(value, label)
    return absolute_path(manifest_directory / PurePosixPath(value))


def verify_expected_hash(evidence: StableFile, expected: str, label: str) -> None:
    if evidence.sha256.lower() != expected.lower():
        fail("LINUX_ARTIFACT_HASH_MISMATCH", f"{label} SHA-256 differs from the manifest")


def read_file_capabilities(descriptor: int) -> bytes:
    try:
        return os.getxattr(descriptor, "security.capability")
    except OSError as error:
        no_attribute = {
            getattr(errno, "ENODATA", -1),
            getattr(errno, "ENOATTR", -1),
            errno.ENOTSUP,
        }
        if error.errno in no_attribute:
            return b""
        fail("LINUX_HELPER_CAPABILITY_CHECK_FAILED", f"cannot read helper file capabilities: {error}")


def inspect_installed_artifact(path: Path, role: str, contract: dict[str, Any]) -> StableFile:
    capture_content = role in {"helperPolicy", "polkitAction"}
    maximum = MAX_POLICY_BYTES if role == "helperPolicy" else MAX_POLKIT_BYTES if role == "polkitAction" else None
    evidence = read_stable_file(
        path,
        f"installed {role}",
        capture_content=capture_content,
        capture_file_capabilities=role == "helper",
        max_bytes=maximum,
    )
    expected_mode = parse_mode(contract["mode"], f"installedArtifacts.{role}.mode")
    if evidence.uid != contract["uid"] or evidence.gid != contract["gid"]:
        fail("LINUX_ARTIFACT_OWNER_INVALID", f"installed {role} must be root:root")
    if evidence.mode != expected_mode:
        fail(
            "LINUX_ARTIFACT_MODE_INVALID",
            f"installed {role} mode is {evidence.mode:04o}, expected {expected_mode:04o}",
        )
    if role == "helper":
        if evidence.mode & (stat.S_ISUID | stat.S_ISGID):
            fail("LINUX_HELPER_PRIVILEGE_BITS_INVALID", "helper must not be setuid or setgid")
        if contract["requireNoFileCapabilities"]:
            if evidence.file_capabilities is None:
                fail(
                    "LINUX_HELPER_CAPABILITY_CHECK_FAILED",
                    "helper capability evidence was not captured from its stable descriptor",
                )
            if evidence.file_capabilities:
                fail("LINUX_HELPER_FILE_CAPABILITIES_INVALID", "helper must not carry file capabilities")
    return evidence


def validate_digest_list(value: Any, label: str) -> list[str]:
    if not isinstance(value, list) or not 1 <= len(value) <= 16 or any(not isinstance(item, str) for item in value):
        fail("LINUX_HELPER_POLICY_INVALID", f"{label} must contain 1-16 SHA-256 strings")
    normalized = [item.lower() for item in value]
    if any(not SHA256_RE.fullmatch(item) for item in normalized) or len(normalized) != len(set(normalized)):
        fail("LINUX_HELPER_POLICY_INVALID", f"{label} contains invalid or duplicate SHA-256 pins")
    return normalized


def verify_helper_policy(content: bytes, contract: dict[str, Any], app_sha256: str, helper_sha256: str) -> None:
    policy = decode_json(content, "installed helper policy")
    require_exact_keys(
        policy,
        {"schemaVersion", "productId", "allowedCallerSha256", "allowedHelperSha256", "allowedRuntimeSha256"},
        "installedHelperPolicy",
    )
    if integer_value(policy, "schemaVersion", "installedHelperPolicy") != contract["schemaVersion"]:
        fail("LINUX_HELPER_POLICY_INVALID", "installed helper policy schemaVersion is wrong")
    if string_value(policy, "productId", "installedHelperPolicy") != contract["productId"]:
        fail("LINUX_HELPER_POLICY_INVALID", "installed helper policy productId is wrong")
    callers = validate_digest_list(policy["allowedCallerSha256"], "allowedCallerSha256")
    helpers = validate_digest_list(policy["allowedHelperSha256"], "allowedHelperSha256")
