"""Verify one installed Linux Sockscap package and emit release evidence.

Lint mode validates the disabled manifest and committed fixed policy without
requiring root, package tools, signatures, or installed artifacts. Release
mode is intentionally same-host: it verifies the exact package signature and
payload, then re-hashes and inspects the fixed installed paths before emitting
a receipt. It does not prove native install/uninstall lab provenance; that
evidence remains an external Gate input. Capture releases are restricted to
the fixed DEB/RPM contract: ordinary AppImage and updater artifacts must keep
capture disabled.
"""

